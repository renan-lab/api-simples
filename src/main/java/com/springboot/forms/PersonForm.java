package com.springboot.forms;

public class PersonForm {

	private String name;
	private Integer age;
	
	public PersonForm() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}
	
	
	
}
